# Scalar Data Structure

print("=== Scalar Example ===")

# Question: Create scalar variables and perform operations

# integer variables
x = 5
y = 3

print("x =", x)
print("Type of x =", type(x))

print("y =", y)
print("Type of y =", type(y))

# addition
print("x + y =", x + y)

print("----------------------")

# float variable
x_float = 3.5

print("x_float =", x_float)
print("Type of x_float =", type(x_float))

# operation with float
print("x_float + y =", x_float + y)
